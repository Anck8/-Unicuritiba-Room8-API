DROP PROCEDURE IF EXISTS SELECT__LUGAR_FOTOS;
DELIMITER $$

CREATE PROCEDURE SELECT__LUGAR_FOTOS(
IN ID INT(5)
)

BEGIN

  DECLARE ID_ INT(5) ;
  DECLARE ID_LUGAR_ INT(5) ;
  DECLARE IMAGEM LONGBLOB;
  
  SET @ID_ = ID; 
  SET @ID_LUGAR_ = (SELECT ID_LUGAR FROM LUGAR WHERE FK_USUARIO = @ID_);

  SELECT 
    FOTOS.IMAGEM_1  
  FROM 
    FOTOS
    
    
  WHERE FOTOS.FK_LUGAR=@ID_LUGAR_;
END$$
DELIMITER ;




