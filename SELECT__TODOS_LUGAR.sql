DROP PROCEDURE IF EXISTS SELECT__TODOS_LUGAR;
DELIMITER $$

CREATE PROCEDURE SELECT__TODOS_LUGAR(
IN ID INT(5)
)

BEGIN

  DECLARE ID_ INT(5) ;
  
  SET @ID_ = ID; 
  
  SELECT 
    LUGAR.TITULO      ,
    LUGAR.DESCRICAO   ,
    LUGAR.CEP         ,
    LUGAR.NUMERO      ,
    LUGAR.COMPLEMENTO ,
    LUGAR.BAIRRO      ,
    LUGAR.CIDADE      ,
    LUGAR.ESTADO      ,
    LUGAR.PONTO_REF   
  FROM 
    LUGAR 
    
   WHERE LUGAR.FK_USUARIO = @ID_;
  



END$$
DELIMITER ;


